/**************************
*10进制转换成二进制 
*除2取余 然后 倒序输出
*
****************************/
#include<stdio.h>//头文件 标准输入输出

int main()
{
    int c[32],i=0,n;//定义数组保存余数
    scanf("%d", &n) ; //输入待转换整数n
    do {
        c[i++]=n%2;
        n/=2;//取余
    } while(n!=0) ;
 
    for(i--;i>=0;i--)//倒序输出
        printf("%d",c[i] );
    printf("\n");
}
//除二取余